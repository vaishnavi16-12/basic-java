package com.bridgelabz.loopingprogram;

import java.util.Scanner;

public class SumOfNaturalNumber {
    public static void main(String[] args) {
        int n=10;
        int sum =0;
        int i = 1;

        while(i <= n){
            sum += i;
            i++;


         }
          System.out.println("Sum Of Natural Number is "+n+ "is: "  +sum );

    }
}
